#multiplicar o comprimento da base e da altura e dividir por dois.
print("14) Peça ao usuário para inserir a base e a altura "
      "de um triângulo. Calcule e exiba a área do triângulo.\n")

base = float(input("Digite a base do triângulo.\n"))
altura = float(input("Digite a altura do triângulo.\n"))
area = (base*altura)/2
print("A área do triângulo é: ", area)